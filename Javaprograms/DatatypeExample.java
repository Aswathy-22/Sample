class DatatypeExample{
public static void main(String args[]){
byte t=2;
short s=50;
int a=100;
long l = 1000;
char letter='r';
float fl=10.01f;
double d=10.123;
boolean b = true;
System.out.println("Byte value is : "+t+"\n"+"Short value is : "+s);  
System.out.println("Integer value is : "+a+"\n"+"Long value is : "+l);    
System.out.println("Character value is : "+letter+"\n"+"Float value is : "+fl);
System.out.println("Double value is : "+d+"\n"+"Boolean value is : "+b);
}
}